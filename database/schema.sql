  ON public.community_reports FOR INSERT
  WITH CHECK (true);

-- ── 7. Spatial Query: Get Nearby Stations ────────────────────
CREATE OR REPLACE FUNCTION public.get_nearby_stations(
  user_lng DOUBLE PRECISION,
  user_lat DOUBLE PRECISION,
  radius_meters DOUBLE PRECISION DEFAULT 10000,
  fuel_filter TEXT DEFAULT NULL
)
RETURNS TABLE (
  station_id      BIGINT,
  station_name    TEXT,
  brand           TEXT,
  city            TEXT,
  state           TEXT,
  lat             DOUBLE PRECISION,
  lng             DOUBLE PRECISION,
  grade_name      TEXT,
  ron_rating      INTEGER,
  ethanol_pct     NUMERIC,
  price_per_litre NUMERIC,
  availability    TEXT,
  last_verified   TIMESTAMPTZ,
  distance_m      DOUBLE PRECISION
)
LANGUAGE sql
STABLE
AS $$
  SELECT
    s.id            AS station_id,
    s.name          AS station_name,
    s.brand,
    s.city,
    s.state,
    s.lat,
    s.lng,
    g.grade_name,
    g.ron_rating,
    g.ethanol_pct,
    g.price_per_litre,
    g.availability_status AS availability,
    g.last_verified,
    ST_Distance(
      s.location,
      ST_SetSRID(ST_MakePoint(user_lng, user_lat), 4326)::geography
    ) AS distance_m
  FROM public.fuel_stations s
  JOIN public.fuel_grades g ON g.station_id = s.id
  WHERE ST_DWithin(
    s.location,
    ST_SetSRID(ST_MakePoint(user_lng, user_lat), 4326)::geography,
    radius_meters
  )
  AND (fuel_filter IS NULL OR g.grade_name = fuel_filter)
  ORDER BY distance_m ASC;
$$;

-- ── 8. Trigger: Auto-update updated_at ───────────────────────
CREATE OR REPLACE FUNCTION update_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_fuel_stations_updated
  BEFORE UPDATE ON public.fuel_stations
  FOR EACH ROW EXECUTE FUNCTION update_updated_at();

-- ── 9. Auto-populate location from lat/lng ───────────────────
CREATE OR REPLACE FUNCTION populate_location()
RETURNS TRIGGER AS $$
BEGIN
  NEW.location = ST_SetSRID(ST_MakePoint(NEW.lng, NEW.lat), 4326)::geography;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_fuel_stations_location
  BEFORE INSERT OR UPDATE ON public.fuel_stations
  FOR EACH ROW EXECUTE FUNCTION populate_location();
