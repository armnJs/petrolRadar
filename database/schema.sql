-- ============================================================
-- Premium Petrol Radar — Supabase Database Schema
-- PostgreSQL + PostGIS for spatial fuel station queries
-- ============================================================

-- ── 1. Enable PostGIS Extension ──────────────────────────────
CREATE EXTENSION IF NOT EXISTS postgis;

-- ── 2. Fuel Stations Table ───────────────────────────────────
CREATE TABLE IF NOT EXISTS public.fuel_stations (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name        TEXT NOT NULL,
  brand       TEXT NOT NULL CHECK (brand IN ('IndianOil', 'HPCL', 'BPCL', 'Shell', 'Nayara', 'Reliance', 'Other')),
  city        TEXT NOT NULL,
  state       TEXT NOT NULL DEFAULT '',
  lat         DOUBLE PRECISION NOT NULL,
  lng         DOUBLE PRECISION NOT NULL,
  location    GEOGRAPHY(POINT, 4326) NOT NULL,
  created_at  TIMESTAMPTZ DEFAULT NOW(),
  updated_at  TIMESTAMPTZ DEFAULT NOW()
);

-- Spatial index for proximity queries
CREATE INDEX IF NOT EXISTS idx_fuel_stations_location
  ON public.fuel_stations USING GIST (location);

-- B-tree indexes for filtering
CREATE INDEX IF NOT EXISTS idx_fuel_stations_brand  ON public.fuel_stations (brand);
CREATE INDEX IF NOT EXISTS idx_fuel_stations_city   ON public.fuel_stations (city);

-- ── 3. Fuel Grades Table (per-station grade availability) ────
CREATE TABLE IF NOT EXISTS public.fuel_grades (
  id                   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  station_id           BIGINT NOT NULL REFERENCES public.fuel_stations(id) ON DELETE CASCADE,
  grade_name           TEXT NOT NULL CHECK (grade_name IN ('XP100', 'Power100', 'Power99', 'Speed97', 'XP95', 'Power95', 'E20', 'E10', 'Standard')),
  ron_rating           INTEGER NOT NULL DEFAULT 87,
  ethanol_pct          NUMERIC(4,1) NOT NULL DEFAULT 20.0,
  price_per_litre      NUMERIC(8,2) NOT NULL,
  availability_status  TEXT NOT NULL DEFAULT 'Available' CHECK (availability_status IN ('Available', 'Limited Stock', 'Out of Stock', 'Just Reported', 'Unverified')),
  last_verified        TIMESTAMPTZ DEFAULT NOW(),
  verified_by          TEXT,
  created_at           TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_fuel_grades_station   ON public.fuel_grades (station_id);
CREATE INDEX IF NOT EXISTS idx_fuel_grades_grade     ON public.fuel_grades (grade_name);
CREATE INDEX IF NOT EXISTS idx_fuel_grades_ron       ON public.fuel_grades (ron_rating);

-- ── 4. Community Reports Table ───────────────────────────────
CREATE TABLE IF NOT EXISTS public.community_reports (
  id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  station_id      BIGINT REFERENCES public.fuel_stations(id) ON DELETE SET NULL,
  user_id         UUID DEFAULT gen_random_uuid(),
  station_name    TEXT NOT NULL,
  brand           TEXT NOT NULL,
  grade_reported  TEXT NOT NULL,
  ron_rating      INTEGER NOT NULL DEFAULT 87,
  ethanol_pct     NUMERIC(4,1) NOT NULL DEFAULT 20.0,
  price_reported  NUMERIC(8,2),
  queue_wait_mins INTEGER DEFAULT 0,
  lat             DOUBLE PRECISION,
  lng             DOUBLE PRECISION,
  notes           TEXT,
  created_at      TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_community_reports_created ON public.community_reports (created_at DESC);

-- ── 5. Daily Prices Table ────────────────────────────────────
CREATE TABLE IF NOT EXISTS public.daily_prices (
  id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  state         TEXT NOT NULL,
  city          TEXT NOT NULL,
  petrol_price  NUMERIC(8,2) NOT NULL,
  diesel_price  NUMERIC(8,2) NOT NULL,
  date          DATE NOT NULL DEFAULT CURRENT_DATE,
  source        TEXT DEFAULT 'manual',
  created_at    TIMESTAMPTZ DEFAULT NOW(),
  UNIQUE (state, city, date)
);

CREATE INDEX IF NOT EXISTS idx_daily_prices_date ON public.daily_prices (date DESC);

-- ── 6. Row Level Security (RLS) Policies ─────────────────────
ALTER TABLE public.fuel_stations    ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.fuel_grades      ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.community_reports ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.daily_prices     ENABLE ROW LEVEL SECURITY;

